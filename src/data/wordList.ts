export const WORD_LIST = new Set(WORD_DATA.split('\n').map(w => w.trim()).filter(w => w.length >= 3));

// Pre-compute word array with frequency maps for fast lookup
const WORD_ARRAY: { word: string; freq: Record<string, number> }[] = [];
for (const word of WORD_LIST) {
  const freq: Record<string, number> = {};
  for (const ch of word) {
    freq[ch] = (freq[ch] || 0) + 1;
  }
  WORD_ARRAY.push({ word, freq });
}

export function findWordsFromLetters(letters: string[]): string[] {
  const available: Record<string, number> = {};
  for (const letter of letters) {
    const upper = letter.toUpperCase();
    available[upper] = (available[upper] || 0) + 1;
  }

  const results: string[] = [];

  for (const { word, freq } of WORD_ARRAY) {
    let canForm = true;
    for (const ch in freq) {
      if ((available[ch] || 0) < freq[ch]) {
        canForm = false;
        break;
      }
    }
    if (canForm) {
      results.push(word);
    }
  }

  results.sort((a, b) => {
    if (b.length !== a.length) return b.length - a.length;
    return a.localeCompare(b);
  });

  return results;
}
